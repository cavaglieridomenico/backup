    if (! apiKeyInfo.has(accountName)) throw new Error(`Account ${accountName} is not mapped`);

    let accountData = JSON.parse(JSON.stringify(apiKeyInfo.get(accountName)));
    if (isPriceApi === true) {
        accountData.hostname = priceApiHostnameBasePath;
    }

    return accountData;
